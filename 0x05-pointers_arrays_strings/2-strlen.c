/**
 * _strlen - It counts the number of characters of the variable value
 *                       and then returns it
 *
 * @s: string parameter input
 *
 * Return: length of value of *s
**/

int _strlen(char *s)
{
	int num;

	num = _strlen(s);

	return (num);
}
